#include<stdio.h>  
#include<string.h>  
#define maxn 100005  
int ans[maxn];  

int main()
{  
  // 打印整数15的十进制、八进制和十六进制表示
  printf("%d\n%o\n%x\n", 15, 15, 15);
  
  // 打印3左移2位的结果（即3乘以2的2次方）
  printf("%d", 3 << 2);
  
  // 打印无符号整数-1的值（在32位系统中为最大值）
  printf("%u\n", -1);
  
  // 打印整数1111
  printf("%d\n", 1111);
  
  // 打印整数1111，宽度为4，不足补零
  printf("%04d\n", 1111);
  
  // 打印浮点数123.456
  printf("%f\n", 123.456);
  
  // 打印浮点数123.456，保留两位小数
  printf("%3.2f\n", 123.456);
  
  // 打印浮点数123.456的科学计数法表示，保留两位小数
  printf("%10.2e\n", 123.456);
  
  // 打印浮点数123.456的科学计数法表示，保留两位小数，左对齐
  printf("%-10.2e\n", 123.456);
  
  // 打印浮点数123.456，带符号
  printf("%+f\n", 123.456);
  
  // 打印浮点数123.456的最简表示
  printf("%g\n", 123.456);
  
  // 打印字符'A'（ASCII值65）
  printf("%c\n", 65);
  
  // 打印字符串"Hello"
  printf("%s\n", "Hello");
  
  // 打印数组ans的内存地址
  printf("%p\n", &ans);
  
  // 打印整数123的十六进制表示
  printf("%x\n", 123);
  
  // 打印整数123的十六进制表示，带前缀0x
  printf("%#x\n", 123);
  
  // 打印整数123的八进制表示，带前缀0
  printf("%#o\n", 123);
  
  // 打印整数123的十六进制表示，带前缀0X（大写）
  printf("%#X\n", 123);
  
  // 打印浮点数123.456的科学计数法表示，带前缀
  printf("%#E\n", 123.456);
  
  // 打印浮点数123.456的最简表示，带前缀
  printf("%#G\n", 123.456);
  
  return 0;  
}